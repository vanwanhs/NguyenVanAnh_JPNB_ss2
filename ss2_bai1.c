#include<stdio.h>
int main(){
	// kieu so nguyen ( int )
	int age = 18;
	printf (" tuoi cua toi la %d\n", age);
	// kieu so thuc ( float )
	float hight = 1.81;
	printf (" chieu cao cua toi la %.2f\n", hight);
	// kieu ki tu ( char )
	 char kyTu = 'a';
	 printf (" Ky tu la %c\n",kyTu);
	 // kieu du lieu ( double )
	 double canNang = 68.99;
	 printf ( " can nang cua toi la %.3f\n", canNang);
	
	
	return 0;
}
